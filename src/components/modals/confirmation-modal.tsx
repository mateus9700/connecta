'use client'

import * as Dialog from '@radix-ui/react-dialog'
import { X } from 'lucide-react'
import { ReactNode } from 'react'
import { Button } from '../button'

interface ConfirmationModalProps {
  children: ReactNode
  variant?: 'safe' | 'danger'
  title: string
  description: string
  onConfirm: () => void
}

export function ConfirmationModal({
  children,
  variant = 'safe',
  title,
  description,
  onConfirm,
}: ConfirmationModalProps) {
  return (
    <Dialog.Root>
      <Dialog.Trigger asChild>{children}</Dialog.Trigger>

      <Dialog.Portal>
        <Dialog.Overlay className="fixed inset-0 z-20 bg-black/60" />

        <Dialog.Content className="fixed inset-4 z-30 m-auto h-fit max-w-lg space-y-5 rounded-2xl bg-white p-5">
          <Dialog.Close asChild>
            <Button size="xs" variant="outline" className="ml-auto">
              <X className="size-5 shrink-0" />
            </Button>
          </Dialog.Close>

          <div className="space-y-2">
            <Dialog.Title className="text-lg font-bold text-zinc-800">
              {title}
            </Dialog.Title>

            <Dialog.Description>{description}</Dialog.Description>
          </div>

          <div className="mt-auto h-px w-full bg-zinc-400" />

          <Button
            variant={variant === 'danger' ? 'danger' : 'primary'}
            onClick={onConfirm}
            className="ml-auto"
          >
            <span>Confirmar</span>
          </Button>
        </Dialog.Content>
      </Dialog.Portal>
    </Dialog.Root>
  )
}
